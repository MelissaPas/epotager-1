<?php

namespace App\Entity;

use App\Repository\OrderRepository;
use Doctrine\ORM\Mapping as ORM;

/**
 * @ORM\Entity(repositoryClass=OrderRepository::class)
 * @ORM\Table(name="`order`")
 */
class Order
{
    /**
     * @ORM\Id
     * @ORM\GeneratedValue
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="integer")
     */
    private $user;

    /**
     * @ORM\Column(type="datetime")
     */
    private $creatDateTime;

    /**
     * @ORM\Column(type="datetime")
     */
    private $payeDateTime;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $number;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $amount;

    /**
     * @ORM\ManyToOne(targetEntity=Items::class, inversedBy="order_id")
     */
    private $items;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getUser(): ?int
    {
        return $this->user;
    }

    public function setUser(int $user): self
    {
        $this->user = $user;

        return $this;
    }

    public function getCreatDateTime(): ?\DateTimeInterface
    {
        return $this->creatDateTime;
    }

    public function setCreatDateTime(\DateTimeInterface $creatDateTime): self
    {
        $this->creatDateTime = $creatDateTime;

        return $this;
    }

    public function getPayeDateTime(): ?\DateTimeInterface
    {
        return $this->payeDateTime;
    }

    public function setPayeDateTime(\DateTimeInterface $payeDateTime): self
    {
        $this->payeDateTime = $payeDateTime;

        return $this;
    }

    public function getNumber(): ?string
    {
        return $this->number;
    }

    public function setNumber(string $number): self
    {
        $this->number = $number;

        return $this;
    }

    public function getAmount(): ?string
    {
        return $this->amount;
    }

    public function setAmount(string $amount): self
    {
        $this->amount = $amount;

        return $this;
    }

    public function getItems(): ?Items
    {
        return $this->items;
    }

    public function setItems(?Items $items): self
    {
        $this->items = $items;

        return $this;
    }
}
